				Theory.ShowCAN();
				break;
			case '5':
				Theory.ShowPastry();
				break;
			case '6':
				Theory.ShowTapestry();
				break;
			default:
				continue;
			}
			Console.Write("Нажмите любую кнопку, чтобы продолжить...");
			Console.ReadKey(true);
		}
	}

