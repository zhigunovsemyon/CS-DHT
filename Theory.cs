	public static void Show()
	{

		for (; ; ) {
			Console.Clear();
			Theory.PrintInfo();
			char ch = Console.ReadKey(true).KeyChar;
			Console.Clear();
			switch (ch) {
			case ESC:
				return;
			case '1':
				Theory.ShowHashing();
				break;
			case '2':
				Theory.ShowDHT();
				break;
			case '3':
				Theory.ShowChord();
				break;
